/*11) Escreva um programa que calcule a média aritmética entre três números reais inseridos pelo usuário. */

const n1 = parseFloat(prompt("Insira um número: "))
const n2 = parseFloat(prompt("Insira outro número: "))
const n3 = parseFloat(prompt("Insira outro número: "))
const media = (n1 + n2 + n3) / 3

alert("A média dos três números inseridos é " + media)
